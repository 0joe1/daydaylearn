def Pfunc(text,text_len,table):
    result=0
    for bit_pos in table:
        result <<= 1
        result |= (text>>(text_len - bit_pos))&0x1
    return result

def Sfunc(text):
    B=[]
    mask = 0x3f
    for i in range(8):
        move = 42-i*6
        B.append(text>>move&mask)

    for i in range(8):
        num = B[i]
        x = ((num&0x20)>>4)|(num&0x1)
        y = (num>>1)&0xf
        B[i] = Sboxes[i][16*x+y]

    ret=0
    for num in B:
        ret = (ret<<4)|(num&0xf)
    return ret

def lrot(val,dis,limit):
    dis %= limit
    ret = (val<<dis)&(2**limit-1) | (val>>(limit-dis))
    return ret

def Kgenerator(seed):
    lrot_values = (1,1,2,2,2,2,2,2,1,2,2,2,2,2,2,1)

    rough = Pfunc(seed,64,PC1)
    keys = {i : None for i in range(17)}

    C0 = (rough>>28)&0xffffffff
    D0 = rough&0xffffffff
    keys[0]=(C0,D0)

    index=0
    for i in range(1,17):
        dis = lrot_values[index]
        keys[i] = (lrot(keys[i-1][0],dis,28),lrot(keys[i-1][1],dis,28))
        index+=1

    for i in range(1,17):
        C_i = keys[i][0]
        D_i = keys[i][1]
        K_i = C_i<<28 | D_i
        keys[i] = Pfunc(K_i,56,PC2)
    return keys


def roundfunc(text,K):
    text = Pfunc(text,32,E)
    text ^= K
    text = Sfunc(text)
    text = Pfunc(text,32,P)
    return text


def encrypt(text,key,en=True):
    keys = Kgenerator(key)
    text = Pfunc(text,64,IP)

    left = (text>>32)&0xffffffff
    right = text&0xffffffff

    if en:
        for i in range(1,17):
            k = keys[i]
            block = roundfunc(right,k)
            left ^= block
            left,right=right,left
    else:
        for i in range(16,0,-1):
            k=keys[i]
            block = roundfunc(right,k)
            left ^= block
            left,right = right,left
    left,right=right,left

    ret = left<<32 | right
    ret = Pfunc(ret,64,IP_INV)

    return ret




